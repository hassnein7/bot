# وظيفة للحصول على معلومات حول موضوع محاسبي معين
def get_accounting_info(topic):
    """الحصول على معلومات حول موضوع محاسبي معين"""
    if topic in accounting_info:
        return accounting_info[topic]
    else:
        # البحث عن كلمات مشابهة
        for key in accounting_info.keys():
            if topic.lower() in key.lower():
                return f"هل تقصد '{key}'؟\n\n{accounting_info[key]}"
        
        return "عذراً، لا توجد معلومات متاحة حول هذا الموضوع. يمكنك استخدام الأمر /topics لعرض قائمة المواضيع المتاحة."

